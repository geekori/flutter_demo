      // 控制是否显示组件
      // Offstage：通过一个变量来控制组件的显示和隐藏
      body:Column(

        children: <Widget>[

          new Offstage(
            offstage: offstage,
            child:Container(color: Colors.blue,height:50.0)

          )
        ],
      ),
      floatingActionButton: new FloatingActionButton(onPressed: (){
        setState(() {
          offstage = !offstage;
          print(offstage);
        });
      },
      tooltip: '显示隐藏',
        child: new Icon(Icons.flip),
      ),


    );
  }
}
